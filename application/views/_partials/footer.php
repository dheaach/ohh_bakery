<script type="text/javascript">
	
	//clear input when modal closed
	$('[data-dismiss=modal]').on('click', function (e) {
	    var $t = $(this),
	        target = $t[0].href || $t.data("target") || $t.parents('.modal') || [];

	  $(target)
	    .find("input,textarea,select")
	       .val('')
	       .end()
	    .find("input[type=checkbox], input[type=radio]")
	       .prop("checked", false)
	       .change();
	   $.uniform.update();
	});

	//function load menu akses
	function load_menus(view = ''){
       $.ajax({
          url:'<?php echo base_url(); ?>login/menus',
          method:"POST",
          data:{view:view},
          dataType:"json",
          success:function(data){
            $('.menus-right').html(data.menus);
            var urs = $('#user-right-session').val();
			var rtbr = urs.split(" ");
			var arrayLength = rtbr.length;
			// console.log(urs);
			for (var i = 0; i < arrayLength; i++) {
			    var xy = document.getElementById(''+rtbr[i]);
		        // console.log(rtbr[i]);
		        if(typeof xy !== 'undefined' && xy !== null) {
		        	xy.style.display = 'block';
		        }
			}
          }
        });
      }

    load_menus();

    //function check user akses
    function check_user_right(eid = ''){
    	var urs = $('#user-right-session').val();
		var rtbr = urs.split(" ");
		var arrayLength = rtbr.length;
		var result = 0;
		
		for (var i = 0; i < arrayLength; i++) {
		    
	        if(rtbr[i] == eid) {
	        	result = 1;
	        }
		}
	    return result;
    }

	// get all menu items
	const menuItems = $('.menu-bar');

	// add click event listener to each menu item
	menuItems.on('click', function() {
		// remove active class from all menu items
		menuItems.removeClass('active');

		// add active class to clicked menu item
		$(this).addClass('active');
		console.log("added");

		// save the active menu item's id to local storage
		localStorage.setItem('activeMenuItem', $(this).attr('id'));
	});

	// check local storage for the active menu item on page load
	const activeMenuItemId = localStorage.getItem('activeMenuItem');

	if (activeMenuItemId) {
		const activeMenuItem = $('#' + activeMenuItemId);
		activeMenuItem.addClass('active');
	}
</script>


